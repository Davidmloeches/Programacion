package Operadores.asignacion;

public class EjemplosAsignacion {
    public static void main(String[] args) {
        //Operador asignacion suma
        int x =10;
        x = x + 2;
        System.out.println(x);
        //otra forma es
        x += 2;
        System.out.println(x);
        //se hace igual para los distintos operadores
    }
}
