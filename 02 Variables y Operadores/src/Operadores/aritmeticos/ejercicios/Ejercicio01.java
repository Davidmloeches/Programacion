package Operadores.aritmeticos.ejercicios;

public class Ejercicio01 {
    public static void main(String[] args) {
        int num1 = 12;
        int num2 = 4;

        //suma
        System.out.println(num1 + num2);

        //resta
        System.out.println(num1 - num2);

        //multiplicacion
        System.out.println(num1 * num2);

        //division
        System.out.println(num1 / num2);

        //modulo
        System.out.println(num1 % num2);
    }
}
