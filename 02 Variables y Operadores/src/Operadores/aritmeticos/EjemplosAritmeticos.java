package Operadores.aritmeticos;

public class EjemplosAritmeticos {
    public static void main(String[] args) {
        //suma
        int a = 13;
        int b = 5;
        int c = a + b;
        System.out.println(c);

        //resta
        System.out.println(a-b);

        //multiplicaci√≥n
        System.out.println(a * b);

        //cociente
        System.out.println(a / b);

        //resto
        System.out.println(a % b);
    }
}
